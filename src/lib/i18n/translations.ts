/** Pick the right language version of a database field. Falls back to IT. */
export function pickLocalized<T extends string | null | undefined>(
  it: T,
  en: T,
  lang: Language,
): T {
  if (lang === "en" && en && String(en).trim().length > 0) return en;
  return it;
}